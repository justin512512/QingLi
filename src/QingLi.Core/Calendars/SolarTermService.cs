    public string? GetName(DateOnly date)
    {
        if (date.Year is < 1901 or > 2100)
        {
            return null;
        }

        var termIndex = (date.Month - 1) * 2;
        var encodedDays = DayTable[date.Year - 1901];

        return date.Day switch
        {
            _ when date.Day == GetDay(encodedDays, termIndex) => TermNames[termIndex],
            _ when date.Day == GetDay(encodedDays, termIndex + 1) => TermNames[termIndex + 1],
            _ => null
        };
    }

    private static int GetDay(string encodedDays, int termIndex)
    {
        var offset = termIndex * 2;
        return ((encodedDays[offset] - '0') * 10) + (encodedDays[offset + 1] - '0');
    }

